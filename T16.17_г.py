def gener(eps, x):
    """Генератор, що повертає всі доданки нескінченної
    суми дійсних чисел за заданим співвідношенням"""

    y = x
    y1 = 0
    k = -1

    while abs(y - y1) > eps:
        y = y1
        dod = (-1) ** (k + 1) * (k + 2) * x ** (k + 1)  # dod - доданок
        yield dod
        y1 += dod
        k += 1

    print('\nСума доданків:', y)


# Перевірка
A = gener(0.0001, 0.45)
print('Доданки:')
for i in A:
    print(i)

